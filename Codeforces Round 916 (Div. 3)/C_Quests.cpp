vector<int> getDivisors(int n)
{
    vector<int> d;
    for (int i = 1; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            d.push_back(i);
            if (i != (n / i))
            {
                d.push_back(n / i);
            }
        }
    }
    return d;
}

void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    for (int i = 0; i < n; i++)
        cin >> b[i];

    int ans = 0, mx = 0, besi = 0;
    for (int i = 0; i < n; i++)
    {
        if (k == i)
            break;
        ans += a[i];
        besi = max(besi, b[i]);
        mx = max(ans + (k - i - 1) * besi, mx);
    }
    cout << mx << endl;
}
