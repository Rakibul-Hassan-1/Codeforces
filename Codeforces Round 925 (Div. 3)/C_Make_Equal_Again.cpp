vector<int> getDivisors(int n)
{
    vector<int> d;
    for (int i = 1; i * i <= n; i++)
    {
        if (n % i == 0)
        {
            d.push_back(i);
            if (i != (n / i))
            {
                d.push_back(n / i);
            }
        }
    }
    return d;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    int j = 0, i = n - 1;
    while (a[j] == a[0])
        j++;

    while (a[i] == a[n - 1])
        i--;

    if (n == 1)
    {
        cout << 0 << endl;
        return;
    }
    if (a.front() != a.back())
    {
        cout << min(n - j, i + 1) << endl;
        return;
    }
    cout << max(0, i - j + 1) << endl;
}
int main()
{
    fasterIO();
    int tc = 1;
    cin >> tc;
    while (tc--)
        solve();

    return 0;
}
